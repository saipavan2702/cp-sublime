const int MOD=1000000007;
const int dx[2] = {1, -1},
          dy[2] = {1, -1};


/*--------------------No.of digits------------------*/
int cntDig(int n) {
	if(n==0)
	return 1;

	return floor(log10(n)+1);
}



/*------------------isPrime--------------*/
int isPrime(int n) {
    bool h=0;
    for (int i=2;i*i<=n;i++) {
        if (n%i==0) {
            h=1;
        }
    }
    return h==0;
}



/*-----------------------check palindrome---------------------------*/
bool check(string s) {
	int n=s.size();
	for(int i=0;i<n/2;i++){
		if(s[i]!=s[n-i-1])
		return 0;
	}
	return 1;
}



/*------------------------print "Yes/No"-------------------*/
void print(bool h){
	if(h)
	cout<<"Yes"<<endl;
	else
	cout<<"No"<<endl;
}



/*------------------------Solve-----------------------------*/
int main(){
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int T;
  cin>>T;
  while(T--){
    int n,m;
    cin>>n>>m;

    vector<vector<int>>v(n,vector<int>(m));
    for(int i=0;i<n;i++){
    	for(int j=0;j<m;j++)
    		cin>>v[i][j];
    }

    ll ans = LLONG_MAX;
    for(int i:dx){
  		for(int j:dy){
	    unordered_map<ll,ll>freq;
	    for(int k=0;k<n;k++){
	      for(int f=0;f<m;f++){
	        ll x=v[k][f]-k*i-f*j;
	        freq[x]++;
	      }
	    }
	    for(auto x:freq) 
	      ans = min(ans, (ll)n*m - x.second);
	    }
	}

    
   

    cout<<ans<<endl;

  }
  return 0;
}

